<?php
// Operadores de incremento e decrecimento

$valor = 20;

// Pré-incremento
echo ++$valor;
echo "<hr>";

// Pos-incremento
echo $valor++;
echo "<br>";
echo $valor;
echo "<hr>";

// Pré-decremento
echo --$valor;
echo "<hr>";

// Pos-incremento
echo $valor--;
echo "<br>";
echo $valor;
echo "<hr>";